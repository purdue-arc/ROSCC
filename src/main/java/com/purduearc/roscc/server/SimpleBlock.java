package com.purduearc.roscc.server;

public class SimpleBlock {
	public final int x;
	public final int y;
	public final int z;
	public final String id;
	public final String nbt;
	public SimpleBlock(int x, int y, int z, String id, String nbt) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.id = id;
		this.nbt = nbt;
	}
	
}
